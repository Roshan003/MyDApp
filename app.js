// Connect to MetaMask
document.getElementById('connectButton').addEventListener('click', async function () {
    if (typeof window.ethereum !== 'undefined') {
        try {
            const accounts = await ethereum.request({ method: 'eth_requestAccounts' });
            userAccount = accounts[0]; // Get the user's first account

            // Instantiate Web3 using MetaMask provider
            web3 = new Web3(window.ethereum);

            // Create contract instance
            contract = new web3.eth.Contract(contractABI, contractAddress);

            // Display connected account
            document.getElementById('status').innerText = 'Wallet Connected: ' + userAccount;

            // Show transfer form and get token balance
            document.getElementById('transferForm').style.display = 'block';
            getTokenBalance();
        } catch (error) {
            document.getElementById('status').innerText = 'Error connecting to MetaMask: ' + error.message;
        }
    } else {
        document.getElementById('status').innerText = 'MetaMask is not installed. Please install it!';
    }
});

// Get Token Balance
async function getTokenBalance() {
    try {
        const balance = await contract.methods.balanceOf(userAccount).call();
        document.getElementById('tokenBalance').innerText = `Token Balance: ${web3.utils.fromWei(balance, 'ether')} MAT`;
    } catch (error) {
        document.getElementById('tokenBalance').innerText = 'Error fetching balance: ' + error.message;
    }
}

// Send Tokens
document.getElementById('sendButton').addEventListener('click', async function () {
    const recipient = document.getElementById('recipient').value;
    const amount = document.getElementById('amount').value;

    if (!web3.utils.isAddress(recipient)) {
        document.getElementById('status').innerText = 'Invalid recipient address';
        return;
    }

    const amountInWei = web3.utils.toWei(amount, 'ether'); // Convert to Wei (adjust if your token has different decimals)

    try {
        const transaction = await contract.methods.transfer(recipient, amountInWei).send({ from: userAccount });
        document.getElementById('status').innerText = `Transaction successful! Hash: ${transaction.transactionHash}`;
        getTokenBalance(); // Refresh balance
    } catch (error) {
        document.getElementById('status').innerText = 'Error sending tokens: ' + error.message;
    }
});
